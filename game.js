function shuffleArray(array) {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];
    }
}

function shuffleArray(array) {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];
    }
}

// Перемішати питання
shuffleArray(questions);

const numberOfQuestions = 25; // Кількість питань за раз
let currentQuestions = questions.slice(0, numberOfQuestions); // Отримати перші 25 питань
let currentQuestionIndex = 0;
let score = 0;
let answered = false;

function showQuestion() {
    const questionElement = document.getElementById('question');
    const answerButtons = document.querySelectorAll('.answer-btn');
    const currentQuestion = currentQuestions[currentQuestionIndex];

    questionElement.textContent = currentQuestion.question;
    answerButtons.forEach((button, index) => {
        button.textContent = currentQuestion.answers[index];
        button.disabled = false; // Розблокувати кнопки для нових питань
        button.style.backgroundColor = ''; // Скидання стилю
    });

    document.getElementById('result').textContent = '';
    answered = false; // Обнулити стан відповіді
}

function checkAnswer(selectedAnswerIndex) {
    if (answered) return; // Перевірка, чи вже відповідали

    const currentQuestion = currentQuestions[currentQuestionIndex];
    const resultElement = document.getElementById('result');
    const answerButtons = document.querySelectorAll('.answer-btn');

    if (selectedAnswerIndex === currentQuestion.correct) {
        score++;
        resultElement.textContent = 'Правильно!';
        resultElement.style.color = 'green';
        answerButtons[selectedAnswerIndex].style.backgroundColor = 'green';
    } else {
        resultElement.textContent = 'Неправильно. Правильна відповідь: ' + currentQuestion.answers[currentQuestion.correct];
        resultElement.style.color = 'red';
        answerButtons[selectedAnswerIndex].style.backgroundColor = 'red';
        answerButtons[currentQuestion.correct].style.backgroundColor = 'green';
    }

    answerButtons.forEach(button => button.disabled = true); // Блокування кнопок після відповіді
    answered = true; // Встановлення стану відповіді
}

function nextQuestion() {
    if (!answered) {
        document.getElementById('result').textContent = 'Виберіть відповідь перед тим, як продовжити!';
        return;
    }

    currentQuestionIndex++;

    if (currentQuestionIndex < currentQuestions.length) {
        showQuestion();
    } else {
        document.getElementById('quiz-container').innerHTML = `
            <h2>Ви завершили вікторину!</h2>
            <p>Ваш результат: ${score} з ${currentQuestions.length} правильних відповідей.</p>
        `;
    }
}

function restartQuiz() {
    currentQuestionIndex = 0;
    score = 0;
    shuffleArray(questions); // Перемішати питання знову
    currentQuestions = questions.slice(0, numberOfQuestions); // Отримати нові 25 питань
    showQuestion();
}

showQuestion();
